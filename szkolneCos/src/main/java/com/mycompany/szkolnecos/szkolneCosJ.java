    private void JBZapiszActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JBZapiszActionPerformed
        //setting var
        String Name = JTImie.getText();
        String Surname = JTNazwisko.getText();
        String Klasa = JCKlasa.getSelectedItem().toString();
        String Przedmiot = JCPrzedmiot.getSelectedItem().toString();
        int Ocena = jSlider1.getValue();
        String Com = Name +";"+Surname+";"+Klasa+";"+Przedmiot+";"+Ocena;
        System.out.println(Com);
        
        //normalize
        String SName = Normalizer.normalize(Name, Normalizer.Form.NFD);
        SName = SName.replaceAll("[\\p{InCombiningDiacriticalMarks}]", "");
        String SSurname = Normalizer.normalize(Surname, Normalizer.Form.NFD);
        SSurname = SSurname.replaceAll("[\\p{InCombiningDiacriticalMarks}]", "");
        
        //Write to file
        PrintWriter writer = null;
        try {
            writer = new PrintWriter(SName+"_"+SSurname+".txt", "UTF-8");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(szkolneCosJ.class.getName()).log(Level.SEVERE, null, ex);
        } catch (UnsupportedEncodingException ex) {
            Logger.getLogger(szkolneCosJ.class.getName()).log(Level.SEVERE, null, ex);
        }
        writer.println(Com);
        writer.close();
    }//GEN-LAST:event_JBZapiszActionPerformed

